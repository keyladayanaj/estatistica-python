# Aula 03 - Mediana
import numpy as np

conjunto_A = [10, 13, 11, 10, 87]
conjunto_B = [23, 31, 17, 18]

# np.median ordena os valores e pega o do meio
print(f'Mediana do conjunto A: {np.median(conjunto_A)}')
print(f'Média do conjunto A: {np.mean(conjunto_A)}')

print(f'Mediana do conjunto B: {np.median(conjunto_B)}')
print(f'Média do conjunto B: {np.mean(conjunto_B)}')


